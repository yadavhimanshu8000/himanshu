// Q15 Calculate sum of 10 numbers using of while loop....

#include<stdio.h>

main()
{
	int i=1,sum=0;								//data member...
	
	printf("sum of ten number...");
	
	while(i<=10)								// using while loop...
	{
		sum=sum+i;
		
		
		
		printf(" %d+ ",i);
		
		i++;									//increment...
	
	}
	printf(" \n \naddtion %d",sum);
	
	
	
}
